							<p style="text-align: left; margin-left: 8em; margin-right: 8em;">
								<b>§1 - Namensgebung</b><br>
								<br>
								1.) Sein Nickname ist frei zu wählen, jedoch sollte er einigermaßen lesbar sein.<br>
								2.) Nicknamen dürfen keine beleidigende, pornographische, rassistische. politische sowie religiöse Inhalte besitzen.<br>
								3.) Nickfakes sind unerwünscht! Dazu gehören auch Nicks, die sinnlos erscheinen oder sind!<br>
								4.) TeamSpeakUser ist als Name nicht gestattet!<br>
								<br>
								<b>§2 - Avatar</b><br>
								<br>
								1.) Avatare dürfen keine rassistischen, beleidigenden oder andere gegen das deutsche Recht verstoßenden Inhalte besitzen.<br>
								2.) Pornographische Inhalte werden geduldet, jedoch bei mehreren Beschwerden entfernt!<br>
								(Ausgenommen sind Inhalte die durch die deutschen Kunstfreiheit geschützt sind.)<br>
								<br>
								<b>§3 - Sicherheit im Internet</b><br>
								<br>
								1.) JEDER trägt die alleinige Verantwortung für das was er/sie postet.<br>
								<br>
								<b>§4 - Nicht stören!</b><br>
								<br>
								§ 4.1 | Gespräche Stören<br>
								1.) User, die Gespräche absichtlich stören und auch nach mehrmaligen Ermahnen keine Einsicht zeigen, müssen mit einem Channel- oder Server Kick rechnen.<br>
								(In uneinsichtigen Fällen werden temporäre Banns verhängt, bis hin zum permanenten Bann.)<br>
								<br>
								§ 4.2 | Geräusche<br>
								1.) Eklige Geräusche oder Rauschen des Headsets sind zu unterlassen!<br>
								2.) Es wird auch gebeten, das Mikro so einzustellen, dass die Atmung nicht dauernd zu hören ist bzw. wenn ihr nebenher esst und ihr habt nichts zu sagen, so deaktiviert bitte euer Mikro.<br>
								<br>
								§ 4.3 | Channel die mit "Nicht stören" gekennzeichnet sind, dürfen nicht ohne Erlaubnis betreten werden!<br>
								<br>
								<b>§5 - Umgangston</b><br>
								<br>
								1.) Der Umgang mit anderen Teamspeak Usern sollte stets freundlich sein.<br>
								2.) Verbale Angriffe gegen andere User sind strengstens untersagt und können bei beschwerde bestraft werden.<br>
								<br>
								<b>§6 - Channel-hopping</b><br>
								<br>
								1.) Das grundlos hin und her switchen eines Channels ist strengstens untersagt! <br>
								(Es kann mit einem Kick oder Ban bestraft werden!)<br>
								<br>
								<b>§7 - Abwesenheit</b><br>
								<br>
								1.) Bei längerer Abwesenheit wird der User gebeten, in den entsprechnden AFK-Channel zu gehen. <br>
								(Bei kürzerer Abwesenheit ist dies nicht nötig.)<br>
								<br>
								<b>§8 - Kicken/Bannen</b><br>
								<br>
								1.) Ein Kick oder Ban ist zu keinem Zeitpunkt unbegründet, sondern soll zum Nachdenken der eigenen Verhaltensweise anregen.<br>
								(Unangebrachte Kicks/Banns müssen den zuständigen Administratoren gemeldet oder an die E-Mail: "<a href="mailto:teamleitung@spastenverein.net">teamleitung@spastenverein.net</a>" gesendet werden.)<br>
								<br>
								<b>§9 - TS3 Rechte</b><br>
								<br>
								1.) Teamspeak Rechte werden nicht wahllos vergeben, sondern dienen immer einem bestimmten Grund.<br>
								(Bei Bedarf von Rechten, kann man sich an einem zuständigen Administrator wenden.)<br>
								<br>
								<b>§10 - Weisungsrecht</b><br>
								<br>
								1.) Administratoren sowie Teammitglieder, haben volles Weisungsrecht! Das verweigern bestimmter Anweisungen, kann zu einem Kick oder Ban führen.<br>
								(Dabei wird "wie im gesamten Netz" auf das Deutsche Grundgesezbuch geachtet.)<br>
								<br>
								<b>§ 10.1 | Kontrollrecht</b> <br>
								<br>
								1.) Admin´s haben das recht zu moven und in jeden geschützten Channel zu joinen.<br>
								<br>
								<b>§11 - Werbung</b><br>
								<br>
								1.) Jegliche Art von Werbung ist auf diesem Server, ohne absprache mit allen Administratoren untersagt. Dabei ist auch das Werben durch Audio-Spuren oder<br>
								über Avatare und Nicknames strengstens untersagt. Ggf. kann sich an einen zuständigen Administratoren gewandt werden, um über eine Möglichkeit zur Werbung zu verhandeln.<br>
								<br>
								<b>§12 - Hackerangriffe</b><br>
								<br>
								1) Jeder Angriff gegen diesen Server ist strafbar und wird ggf. strafrechtlich verfolgt und immer mit einem sofortigen permanenten Ban von allen Servern und plattformen beglitten.<br>
								<br>
								<b>§13 - Datenschutz</b><br>
								<br>
								1.) Private Daten wie Telefonnummern, Adressen, Passwörter, IP-Adressen und ähnlichem dürfen nicht öffentlich ausgetauscht werden.<br>
								2.) Ein Server Admin wird zu keinem Zeitpunkt nach sensiblen Daten, wie Benutzer-Passwörter, fragen.<br>
								(Soeinen Vorfall bitte umgehend im Support, bei der Teamleitung oder an die E-Mail: "<a href="mailto:admin@spastenverein.net">admin@spastenverein.net</a>" melden!)<br>
								<br>
								<b>§14 - Soundbords</b><br>
								<br>
								1.) Das Nutzen von Soundboards wird geduldet!<br>
								(Wird dies allerdings für Earrapes oder zum puren nerven genutzt, kann dies je nach situation mit einem Kick oder sogar einem Ban mit bis zu 48h beschtraft werden.)<br>
								2.) Nach §4 kann man allerdings mit einer längeren Strafe rechnen wenn man beleidigende, nazistische, rassistische usw. sounds abgespielt.<br>
								<br>
								<b>§15 - User Bots</b><br>
								<br>
								1.) Bots dürfen nur mit der ausdrücklichen erlaubnis eines Administratoren auf den Teamspek geholt werden!<br>
								<br>
								<b>§16 - Upload von Dateien</b><br>
								<br>
								1.) Es ist nicht gestattet rassistische, beleidigende oder rechtlich geschütztes Material hochzuladen und/oder mit anderen Benutzern zu tauschen.<br>
								(Ausgenommen sind Inhalte die durch die deutschen Kunstfreiheit geschützt sind.)<br>
								<br>
								<b>§17 - Meldepflicht</b><br>
								<br>
								1.) Es sind alle Benutzer angehalten, die TS3-Server Regeln zu beachten.<br>
								(Sollte ein Regelverstoß von einem Benutzer erkannt werden, ist dieser umgehend im Support zu melden, sollte dies nicht getan werden kann eine Person als Mittäter gesehen werden!)<br>
								<br>
								<b>§18 - Gespräche aufnehmen/streamen</b><br>
								<br>
								1.) Gespräche aufnehmen oder zu streamen sind verboten! <br>
								(Außer in Channeln, die extra dafür erstellt wurden sind oder im Channel-Namen Aufnahme/Live stehen haben.)<br>
								<br>
								<b>§19 - Privatsphäre</b><br>
								<br>
								1.) Alle User können §10.1 umgehen wenn sie ein Privates Gespräch führen.<br>
								(Bei einem Privaten gespräch gelten aber §5,§14 und §17 nicht!)<br>
